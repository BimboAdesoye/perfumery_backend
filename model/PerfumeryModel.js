const mongoose = require("mongoose");
const Schema = mongoose.Schema;

const perfumeSchema = new Schema({
  title: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  category: {
    type: String,
    required: true,
  },
  label: {
    type: String,
    required: true,
  },
  image: {
    type: String,
    required: true,
  },
  price: {
    type: Number,
    required: true,
  },
});

const PERFUMES = mongoose.model("singlePerfume", perfumeSchema);

module.exports = PERFUMES;
